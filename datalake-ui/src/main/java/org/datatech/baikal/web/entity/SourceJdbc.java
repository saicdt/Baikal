package org.datatech.baikal.web.entity;

import java.io.Serializable;

public class SourceJdbc implements Serializable {

    private String row_key;
    private String instance_name;
    private String schema_name;
    private String db_type;
    private String jdbc_url;
    private String user;
    private String password;
    private String class_name;
    private String os_ip;
    private String rmt_ip;

    public String getRow_key() {
        return row_key;
    }

    public void setRow_key(String row_key) {
        this.row_key = row_key;
    }

    public String getInstance_name() {
        return instance_name;
    }

    public void setInstance_name(String instance_name) {
        this.instance_name = instance_name;
    }

    public String getSchema_name() {
        return schema_name;
    }

    public void setSchema_name(String schema_name) {
        this.schema_name = schema_name;
    }

    public String getDb_type() {
        return db_type;
    }

    public void setDb_type(String db_type) {
        this.db_type = db_type;
    }

    public String getJdbc_url() {
        return jdbc_url;
    }

    public void setJdbc_url(String jdbc_url) {
        this.jdbc_url = jdbc_url;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getClass_name() {
        return class_name;
    }

    public void setClass_name(String class_name) {
        this.class_name = class_name;
    }

    public String getOs_ip() {
        return os_ip;
    }

    public void setOs_ip(String os_ip) {
        this.os_ip = os_ip;
    }

    public String getRmt_ip() {
        return rmt_ip;
    }

    public void setRmt_ip(String rmt_ip) {
        this.rmt_ip = rmt_ip;
    }
}
